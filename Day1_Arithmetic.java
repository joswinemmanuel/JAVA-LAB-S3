public class Day1_Arithmetic {

	public static void main(String[] args) {
		int a=20, b=10;
		System.out.println(a+" + "+b+" = "+(a+b));
		System.out.println(a+" - "+b+" = "+(a-b));
		System.out.println(a+" * "+b+" = "+(a*b));
		System.out.println(a+" / "+b+" = "+(a/b));

	}

}

/*
20 + 10 = 30
20 - 10 = 10
20 * 10 = 200
20 / 10 = 2
*/